package helpers;

public class Formulario {
	private Long idSucursal;
	private Long idSocio;
	private Long idSucursalActividad;
	private Long idPase;
	
	private String prueba;
	
	
	
	
	public Long getIdPase() {
		return idPase;
	}

	public void setIdPase(Long idPase) {
		this.idPase = idPase;
	}

	public String getPrueba() {
		return prueba;
	}

	public void setPrueba(String prueba) {
		this.prueba = prueba;
	}

	public Long getIdSocio() {
		return idSocio;
	}

	public void setIdSocio(Long idSocio) {
		this.idSocio = idSocio;
	}

	public Long getIdSucursalActividad() {
		return idSucursalActividad;
	}

	public void setIdSucursalActividad(Long idSucursalActividad) {
		this.idSucursalActividad = idSucursalActividad;
	}

	public Long getIdSucursal() {
		return idSucursal;
	}

	public void setIdSucursal(Long idSucursal) {
		this.idSucursal = idSucursal;
	}
	
	
}