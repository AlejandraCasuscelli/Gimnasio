package helpers;

public class MercadoPagoMetadata {
    private Long id_pase;
    private Long id_socio;
    private Long id_descuento;

    public Long getId_pase() {
        return id_pase;
    }

    public void setId_pase(Long id_pase) {
        this.id_pase = id_pase;
    }

    public Long getId_socio() {
        return id_socio;
    }

    public void setId_socio(Long id_socio) {
        this.id_socio = id_socio;
    }

    public Long getId_descuento() {
        return id_descuento;
    }

    public void setId_descuento(Long id_descuento) {
        this.id_descuento = id_descuento;
    }
}
